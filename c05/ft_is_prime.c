int	ft_is_prime(int nb)
{
	int	d;

	d = 2;
	if (nb <= 2)
		return (0);
	else if (nb == 2147483647)
		return (1);
	while (d < nb)
	{
		if (!(d % 2 || d % 3 || d % 5))
			d++;
		if (!(nb % d))
			return (0);
		d++;
	}
	return (1);
}
